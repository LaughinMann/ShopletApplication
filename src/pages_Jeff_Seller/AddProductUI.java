    private void create_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_create_buttonActionPerformed
        boolean match = false;
        if(String.valueOf(product_name_text.getText()).isEmpty()){
            if(String.valueOf(price_text.getText()).isEmpty()){
                if(String.valueOf(quantity_text.getText()).isEmpty()){
                    if(String.valueOf(description_text.getText()).isEmpty()){
                        match = false;
                        JFrame frame = new JFrame();
                        JOptionPane.showMessageDialog(frame, "You left a field empty!");
                    }
                }
            }
        }
        else {
            match = true;
            SellersPanelUI obj = new SellersPanelUI();
            obj.setVisible(true);
            JFrame frame = new JFrame();
            JOptionPane.showMessageDialog(frame, "Item successfully added.");
            database create = new database();
		    create.add_product(product_name_text.getText(), price_text.getText(), String.valueOf(quantity_text.getText()), String.valueOf(description_text.getText()), match);
        }
    }//GEN-LAST:event_create_buttonActionPerformed

